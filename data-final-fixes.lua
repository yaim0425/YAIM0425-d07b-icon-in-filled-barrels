function This_MOD.get_elements()
    --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---
    --- Función para analizar cada entidad
    --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---

    local function valide_recipe(recipe)
        --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---
        --- Validación
        --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---

        --- Validar la receta
        if not recipe then return end
        if not recipe.results then return end
        if not recipe.ingredients then return end
        if GMOD.is_hidde(recipe) then return end

		---  Cantidad de entrada y salida
        if #recipe.results ~= 1 then return end
        if #recipe.ingredients ~= 2 then return end

		---  Elementos de entrada y salida
		local Item  = GMOD.get_tables(recipe.ingredients, "name", "barrel")
        local Fuild = GMOD.get_tables(recipe.ingredients, "type", "fluid")
        if not Item then return end
        if not Fuild then return end

        --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---





        --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---
        --- Valores para el proceso
        --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---

        local Space = {}
        Space.item = GMOD.items[recipe.results[1].name]
        Space.recipe = recipe

        --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---





        --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---
        --- Guardar la información
        --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---

        This_MOD.to_be_processed.barrel = This_MOD.to_be_processed.barrel or {}
        This_MOD.to_be_processed.barrel[Space.item.name] = Space

        --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---
    end

    --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---





    --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---
    --- Preparar los datos a usar
    --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---

    for _, recipe in pairs(data.raw.recipe) do
        valide_recipe(recipe)
    end

	--- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- --- ---
end

